function drawTheWayOut(board) {
  let position = []
  let char = ''
  for (let i = 0; i < board.length; i++) {
    for (let j = 0; j < board[i].length; j++) {
      if (board[i][j] !== '*' && board[i][j] !== ' ') {
        position = [i, j]
        char = board[i][j]
        break
      }
    }
  }
  
  do {
    position = getNextStep(position, board)
    if (position) {
      char = getNextChar(char)
      board[position[0]][position[1]] = char
    }
  } while (position)

  console.log(board)
}

function getNextStep(position, board) {
  let [i, j] = position
  if (board[i - 1] && board[i - 1][j] === ' ') {
    return [i - 1, j]
  } else if (board[i][j + 1] === ' ') {
    return [i, j + 1]
  } else if (board[i + 1] && board[i + 1][j] === ' ') {
    return [i + 1, j]
  } else if (board[i][j - 1] === ' ') {
    return [i, j - 1]
  } else {
    return void 0
  }
}

function getNextChar(char) {
  let nextCharCode = char.charCodeAt(0) + 1
  if (nextCharCode > 90) nextCharCode = 65
  return String.fromCharCode(nextCharCode)
}


